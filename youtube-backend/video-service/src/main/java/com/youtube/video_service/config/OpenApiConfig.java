package com.youtube.video_service.config;

import io.swagger.v3.oas.models.ExternalDocumentation;
import io.swagger.v3.oas.models.OpenAPI;
import io.swagger.v3.oas.models.info.Info;
import io.swagger.v3.oas.models.info.License;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

@Configuration
public class OpenApiConfig {
    @Bean
    public OpenAPI videoServiceApi(){
        return new OpenAPI()
                .info(new Info().title("Video Service Api")
                        .description("This is the REST API for Video Service")
                        .version("v0.0.1")
                        .license(new License().name("Apache 2.0")))
                .externalDocs(new ExternalDocumentation()
                        .description("You can refer to the Video Service wiki Documentation")
                        .url("http://video-service-dummy-url.com/docs"));
    }
}
